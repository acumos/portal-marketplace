package org.acumos.portal.be.transport;

public class MSGeneration {

	String solutioId;
	String revisionId;
	String provider;
	String authorization;
	String trackingID;
	String modName;
	String deploymentEnv;
	String requestId;
	boolean deploy;
	
	public String getSolutioId() {
		return solutioId;
	}
	public void setSolutioId(String solutioId) {
		this.solutioId = solutioId;
	}
	public String getRevisionId() {
		return revisionId;
	}
	public void setRevisionId(String revisionId) {
		this.revisionId = revisionId;
	}
	public String getProvider() {
		return provider;
	}
	public void setProvider(String provider) {
		this.provider = provider;
	}
	public String getAuthorization() {
		return authorization;
	}
	public void setAuthorization(String authorization) {
		this.authorization = authorization;
	}
	public String getTrackingID() {
		return trackingID;
	}
	public void setTrackingID(String trackingID) {
		this.trackingID = trackingID;
	}
	public String getModName() {
		return modName;
	}
	public void setModName(String modName) {
		this.modName = modName;
	}
	public String getDeploymentEnv() {
		return deploymentEnv;
	}
	public void setDeploymentEnv(String deploymentEnv) {
		this.deploymentEnv = deploymentEnv;
	}
	public String getRequestId() {
		return requestId;
	}
	public void setRequestId(String requestId) {
		this.requestId = requestId;
	}
	public boolean isDeploy() {
		return deploy;
	}
	public void setDeploy(boolean deploy) {
		this.deploy = deploy;
	}
	
}
